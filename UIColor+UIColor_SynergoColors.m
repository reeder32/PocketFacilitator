#import "UIColor+UIColor_SynergoColors.h"

@implementation UIColor (UIColor_SynergoColors)

+ (UIColor *)synergoBlueColor{
    return [UIColor colorWithRed:0.15f green:0.21f blue:0.37f alpha:1.0f];
}

+ (UIColor *)synergoRedColor{
    return [UIColor colorWithRed:0.40f green:0.00f blue:0.06f alpha:1.0f];
}

+ (UIColor *)synergoBrownColor{
    return [UIColor colorWithRed:0.28f green:0.18f blue:0.18f alpha:1.0f];
}

+ (UIColor *)synergoGrayColor{
    return [UIColor colorWithRed:0.86f green:0.87f blue:0.86f alpha:1.0f];
}
@end
