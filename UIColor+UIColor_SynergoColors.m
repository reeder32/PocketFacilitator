#import "UIColor+UIColor_SynergoColors.h"

@implementation UIColor (UIColor_SynergoColors)

+ (UIColor *)synergoDarkGrayColor{
    return [UIColor colorWithRed:0.47f green:0.45f blue:0.47f alpha:1.0f];
}

+ (UIColor *)synergoRedColor{
    return [UIColor colorWithRed:0.82f green:0.33f blue:0.26f alpha:1.0f];
}

+ (UIColor *)synergoMaroonColor{
    return [UIColor colorWithRed:0.51f green:0.27f blue:0.25f alpha:1.0f];
}

+ (UIColor *)synergoLightGrayColor{
    return [UIColor colorWithRed:0.75f green:0.80f blue:0.80f alpha:1.0f];
}
@end
